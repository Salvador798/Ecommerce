    /**
     * Display a listing of the resource.
     */
    public function index()
    {
        if (Auth::id()) {
            $user = Auth::user();
            $userId = $user->id;
            $count = Cart::where('user_id', $userId)->count();
            $cart = Cart::where('user_id', $userId)->get();
        }
        return view('home.Cart.index', compact('count', 'cart'));
    }

